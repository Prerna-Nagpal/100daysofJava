// use of compund assignment operators in Java
public class task2 {
    public static void main(String[] args) {
        int num =10;
        num+=5;
        System.out.println("After adding 5: " + num);
        num-=3;
        System.out.println("After subtracting 3: " + num);
        num*=2;
        System.out.println("After multiplying by 2: " + num);
        num/=4;
        System.out.println("After dividing by 4: " + num);
        num%=3;
        System.out.println("After modulus by 3: " + num);
    }
}
